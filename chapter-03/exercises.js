function countChars(string,char) {
    // create variable count assign to 0
  var count = 0;
      //create for loop to loop through string
  for (var i = 0; i < string.length; i++){
          //if string at each index has char
if(string[i] === char){
  //add 1 to the count
count++
  }
    }
    //return count
  return count
  }
  console.log(countChars("kakkerlak", "k"));


