function isEven(n) {
  //if n is even
    if(n === 0){
      //return true
      return true
  }
  // if n is odd
    if(n === 1){
      //return false
      return false
      }
      // n is negative
    if(n < 0){
      //Convert negative to positive
      return isEven(-n)
      }
      // calls itself - 2 for even
    return isEven(n - 2)
    }
  console.log(isEven(50));
  // → true
  console.log(isEven(75));
  // → false
  console.log(isEven(-1));
  // → ??


